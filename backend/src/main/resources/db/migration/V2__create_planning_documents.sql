CREATE TABLE planning_documents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id BIGINT NOT NULL,
    title VARCHAR(255) NOT NULL,
    original_file_name VARCHAR(255) NOT NULL,
    stored_file_path VARCHAR(1000),
    mime_type VARCHAR(100) NOT NULL,
    file_size BIGINT NOT NULL,
    page_count INTEGER,
    extracted_text TEXT,
    page_contents JSONB,
    processing_status VARCHAR(20) NOT NULL,
    failure_reason TEXT,
    created_by BIGINT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_planning_documents_project FOREIGN KEY (project_id) REFERENCES projects (id),
    CONSTRAINT fk_planning_documents_creator FOREIGN KEY (created_by) REFERENCES users (id),
    CONSTRAINT chk_planning_documents_status CHECK (processing_status IN ('UPLOADED', 'PROCESSING', 'READY', 'FAILED'))
);
