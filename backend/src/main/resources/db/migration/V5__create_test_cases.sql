CREATE TABLE test_cases (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id BIGINT NOT NULL,
    planning_document_id BIGINT NOT NULL,
    analysis_job_id BIGINT NOT NULL,
    requirement_id BIGINT NOT NULL,
    external_test_case_id VARCHAR(100) NOT NULL,
    display_order INTEGER NOT NULL,
    major_category VARCHAR(255) NOT NULL,
    middle_category VARCHAR(255) NOT NULL,
    minor_category VARCHAR(255) NOT NULL,
    test_item VARCHAR(500) NOT NULL,
    test_type VARCHAR(30) NOT NULL,
    priority VARCHAR(30) NOT NULL,
    confidence VARCHAR(10) NOT NULL,
    status VARCHAR(20) NOT NULL,
    preconditions JSONB NOT NULL,
    test_steps JSONB NOT NULL,
    expected_results JSONB NOT NULL,
    evidences JSONB NOT NULL,
    notes JSONB NOT NULL,
    requires_human_review BOOLEAN NOT NULL,
    reviewed_by BIGINT,
    reviewed_at TIMESTAMP,
    rejection_reason TEXT,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_test_cases_project FOREIGN KEY (project_id) REFERENCES projects (id),
    CONSTRAINT fk_test_cases_document FOREIGN KEY (planning_document_id) REFERENCES planning_documents (id),
    CONSTRAINT fk_test_cases_analysis_job FOREIGN KEY (analysis_job_id) REFERENCES analysis_jobs (id),
    CONSTRAINT fk_test_cases_requirement FOREIGN KEY (requirement_id) REFERENCES requirements (id),
    CONSTRAINT fk_test_cases_reviewer FOREIGN KEY (reviewed_by) REFERENCES users (id),
    CONSTRAINT uq_test_cases_job_external_id UNIQUE (analysis_job_id, external_test_case_id),
    CONSTRAINT uq_test_cases_job_display_order UNIQUE (analysis_job_id, display_order),
    CONSTRAINT chk_test_cases_type CHECK (test_type IN ('HAPPY_PATH', 'BOUNDARY', 'EXCEPTION', 'VALIDATION', 'STATE_TRANSITION', 'TIME', 'DUPLICATION', 'DATA_PERSISTENCE')),
    CONSTRAINT chk_test_cases_confidence CHECK (confidence IN ('HIGH', 'MEDIUM', 'LOW')),
    CONSTRAINT chk_test_cases_status CHECK (status IN ('GENERATED', 'APPROVED', 'REJECTED'))
);
