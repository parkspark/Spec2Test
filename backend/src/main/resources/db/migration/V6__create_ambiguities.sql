CREATE TABLE ambiguities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    planning_document_id BIGINT NOT NULL,
    analysis_job_id BIGINT NOT NULL,
    external_ambiguity_id VARCHAR(100) NOT NULL,
    major_category VARCHAR(255) NOT NULL,
    middle_category VARCHAR(255) NOT NULL,
    minor_category VARCHAR(255) NOT NULL,
    title VARCHAR(500) NOT NULL,
    description TEXT NOT NULL,
    question TEXT NOT NULL,
    impact TEXT NOT NULL,
    severity VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL,
    evidences JSONB NOT NULL,
    jira_issue_key VARCHAR(255),
    jira_issue_url VARCHAR(1000),
    published_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_ambiguities_document FOREIGN KEY (planning_document_id) REFERENCES planning_documents (id),
    CONSTRAINT fk_ambiguities_analysis_job FOREIGN KEY (analysis_job_id) REFERENCES analysis_jobs (id),
    CONSTRAINT uq_ambiguities_job_external_id UNIQUE (analysis_job_id, external_ambiguity_id),
    CONSTRAINT chk_ambiguities_status CHECK (status IN ('OPEN', 'ISSUE_CREATED', 'DISMISSED'))
);
